/* ══ ISO GRID ══ */
(function(){
  const cv=document.getElementById('bg-grid'),ctx=cv.getContext('2d');let t=0;
  function resize(){cv.width=window.innerWidth;cv.height=window.innerHeight;}
  resize();window.addEventListener('resize',resize);
  const TW=80,TH=40;
  function iso(c,r,w,h){return{x:w*.5+(c-r)*(TW*.5),y:h*.28+(c+r)*(TH*.5)};}
  function draw(){
    const w=cv.width,h=cv.height;ctx.clearRect(0,0,w,h);
    for(let r=-2;r<28;r++)for(let c=-2;c<28;c++){
      const d=Math.sqrt((c-14)**2+(r-14)**2),fade=Math.max(0,1-d/(14*1.41*.85));
      const p=(Math.sin(t*.6-d*.28)*.5+.5)*fade,a=(.06+p*.18)*fade;
      const tl=iso(c,r,w,h),tr=iso(c+1,r,w,h),br=iso(c+1,r+1,w,h),bl=iso(c,r+1,w,h);
      ctx.beginPath();ctx.moveTo(tl.x,tl.y);ctx.lineTo(tr.x,tr.y);ctx.lineTo(br.x,br.y);ctx.lineTo(bl.x,bl.y);ctx.closePath();
      ctx.fillStyle=`rgba(255,255,255,${p*.025})`;ctx.fill();
      ctx.strokeStyle=`rgba(255,255,255,${a})`;ctx.lineWidth=.5;ctx.stroke();
    }
    t+=.014;requestAnimationFrame(draw);
  }draw();
})();

