/* ══ ACTIVATE ══ */
function activate(idx,instant=false){
  if(busy&&!instant)return;
  busy=true;
  current=idx;
  const p=PROJECTS[idx];

  // update rail active
  document.querySelectorAll('.lp-row').forEach(r=>{
    r.classList.toggle('active', +r.dataset.id===p.id);
  });
  // scroll active row into view
  const activeRow=lpList.querySelector('.lp-row.active');
  if(activeRow) activeRow.scrollIntoView({block:'nearest',behavior:'smooth'});

  // progress
  stageFill.style.width=((idx+1)/PROJECTS.length*100)+'%';

  // topbar
  sbCat.textContent=p.cat;
  sbCat.className='sb-cat '+CAT_CLASS[p.cat];
  sbYear.textContent=p.year;
  sbCur.textContent=String(idx+1).padStart(2,'0');

  hide();

  setTimeout(()=>{
    // glow
    stageGlow.style.background=p.glow;

    // ghost num
    siGhost.textContent=String(p.id).padStart(2,'0');

    // info
    siNum.textContent=String(p.id).padStart(2,'0')+' — '+p.cat+' · '+p.year;
    siTitle.textContent=p.title;
    siSub.textContent=p.sub;
    siBullets.innerHTML=(p.bullets||[]).map(b=>`<li class="si-bullet"><span class="si-bullet-dot"></span><span>${b}</span></li>`).join('');
    siTags.innerHTML=p.tags.map(t=>`<span class="si-tag">${t}</span>`).join('');

    const hasLink=p.link&&p.link!=='#';
    siActions.innerHTML=`
      <a href="${p.github}" target="_blank" rel="noopener" class="si-cta si-cta--solid">GitHub →</a>
      ${hasLink?`<a href="${p.link}" target="_blank" rel="noopener" class="si-cta si-cta--ghost">Live Site →</a>`:''}`;

    // browser
    spUrl.textContent=hasLink?p.link.replace('https://',''):p.title.toLowerCase().replace(/ /g,'_')+'.py';
    spLink.href=hasLink?p.link:'#';
    spLink.style.opacity=hasLink?'1':'.18';
    spScreen.innerHTML='';
    if(hasLink){
      const iframe=document.createElement('iframe');
      iframe.src=p.link;
      iframe.setAttribute('sandbox','allow-scripts allow-same-origin allow-forms');
      iframe.loading='lazy';
      spScreen.appendChild(iframe);
    }else{
      const cv=getCanvas(p);
      spScreen.appendChild(cv);
      setTimeout(()=>animCanvas(cv,p),60);
    }

    show();
    setTimeout(()=>{busy=false;},700);
  },instant?0:55);
}

