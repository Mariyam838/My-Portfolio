function animCanvas(canvas,p){
  if(canvas._running)return;
  canvas._running=true;
  canvas.width=canvas.offsetWidth||700;
  canvas.height=canvas.offsetHeight||450;
  const ctx=canvas.getContext('2d'),w=canvas.width,h=canvas.height;
  const slug=p.title.toLowerCase().replace(/ /g,'_');
  let t=0;
  const lines=[
    {type:'kw',kw:'def ',fn:slug+'():',body:''},
    {type:'cm',text:'    # '+p.sub},
    {type:'code',text:'    data = load_records()'},
    {type:'kw',kw:'    for ',fn:'item',body:' in data:'},
    {type:'code',text:'        result = process(item)'},
    {type:'kw',kw:'        print',fn:'',body:'(result)'},
    {type:'blank'},
    {type:'kw',kw:'if ',fn:'__name__',body:'=="__main__":'},
    {type:'code',text:'    '+slug+'()'},
  ];
  function draw(){
    ctx.fillStyle='#0b0b16';ctx.fillRect(0,0,w,h);
    ctx.fillStyle='#10102a';ctx.fillRect(0,0,52,h);
    const sy=(t*.8)%h;ctx.fillStyle='rgba(247,201,126,.011)';ctx.fillRect(0,sy,w,2);
    lines.forEach((l,li)=>{
      const y=62+li*46,fs='14px';
      ctx.font=fs+' DM Mono,monospace';ctx.textAlign='right';
      ctx.fillStyle='rgba(255,255,255,.1)';ctx.fillText(li+1,42,y);ctx.textAlign='left';
      if(l.type==='blank')return;
      if(l.type==='cm'){ctx.fillStyle='#4e6a55';ctx.fillText(l.text,62,y);}
      else if(l.type==='kw'){
        ctx.fillStyle='#c792ea';ctx.font='600 '+fs+' DM Mono,monospace';ctx.fillText(l.kw,62,y);
        const x1=62+ctx.measureText(l.kw).width;
        ctx.fillStyle='#82aaff';ctx.font=fs+' DM Mono,monospace';ctx.fillText(l.fn,x1,y);
        ctx.fillStyle='#cdd3de';ctx.fillText(l.body,x1+ctx.measureText(l.fn).width,y);
      }else{ctx.fillStyle='#cdd3de';ctx.font=fs+' DM Mono,monospace';ctx.fillText(l.text,62,y);}
    });
    // terminal strip
    const th=100;
    ctx.fillStyle='#060610';ctx.fillRect(0,h-th,w,th);
    ctx.fillStyle='rgba(247,201,126,.08)';ctx.fillRect(0,h-th,w,1);
    ctx.font='12px DM Mono,monospace';ctx.textAlign='left';
    ctx.fillStyle='rgba(255,255,255,.2)';ctx.fillText('$ python '+slug+'.py',16,h-th+22);
    ctx.fillStyle='#9ef77e';ctx.fillText('→  '+p.sub,16,h-th+44);
    ctx.fillStyle='rgba(255,255,255,.25)';ctx.fillText('   '+p.tags.join(' · '),16,h-th+66);
    if(Math.floor(t/24)%2===0){ctx.fillStyle='#9ef77e';ctx.fillText('█',16,h-th+88);}
    t++;requestAnimationFrame(draw);
  }draw();
}

